class Command(BaseCommand):
    help = "시연용 사연 45건 시드 (카테고리당 5건, 재실행 안전)."

    def handle(self, *args, **options):
        added = 0
        updated = 0
        for cat_slug, username, view_count, is_anon, title, content in DEMO:
            try:
                user = User.objects.get(username=username)
                cat = Category.objects.get(slug=cat_slug)
            except (User.DoesNotExist, Category.DoesNotExist):
                continue

            obj, created = Story.all_objects.update_or_create(
                title=title,
                defaults={
                    "user": user,
                    "content": content,
                    "category": cat,
                    "is_anonymous": is_anon,
                    "view_count": view_count,
                    "is_deleted": False,
                    "deleted_at": None,
                },
            )
            if created:
                added += 1
            else:
                updated += 1

        total = Story.objects.count()
        self.stdout.write(
            self.style.SUCCESS(
                f"신규 {added}건, 갱신 {updated}건 / 전체 active Story = {total}건"
            )
        )
